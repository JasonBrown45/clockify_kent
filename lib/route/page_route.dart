import 'package:flutter/material.dart';

import '../auth/presentation/email_page.dart';
import '../auth/presentation/password_page.dart';
import '../auth/presentation/register_page.dart';

Route createEmailPageRoute(email) {
  return PageRouteBuilder(
    pageBuilder: (context, animation, secondaryAnimation) => const EmailPage(),
    transitionsBuilder: (context, animation, secondaryAnimation, child) {
      const begin = Offset(1.0, 0.0);
      const end = Offset.zero;
      const curve = Curves.ease;

      var tween = Tween(begin: begin, end: end).chain(CurveTween(curve: curve));

      return SlideTransition(
        position: animation.drive(tween),
        child: child,
      );
    },
  );
}

